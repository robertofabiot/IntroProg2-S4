# Pide una edad actual y muestra qué edad tendrá el usuario dentro 
# de 5, 10 y 20 años.

edad_actual = int(input("Introduce tu edad actual: "))
edad_cinco = edad_actual + 5
edad_diez = edad_actual + 10
edad_veinte = edad_actual + 20

print(f"Tu edad actual es: {edad_actual} años")
print(f"Tu edad en cinco años será: {edad_cinco} años")
print(f"Tu edad en diez años será: {edad_diez} años")
print(f"Tu edad en veinte años será: {edad_veinte} años")
