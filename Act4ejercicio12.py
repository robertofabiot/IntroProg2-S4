# El dueño de una tienda compra un artículo a un precio determinado. Obtener el precio 
# en que lo debe vender para obtener una ganancia del 30%. 

precio_compra = float(input("Ingrese el precio de compra del artículo: "))
precio_venta = precio_compra * 1.3

print(f"El precio de venta para obtener una ganancia del 30% es: {precio_venta:.2f}")

